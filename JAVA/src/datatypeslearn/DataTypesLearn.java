package datatypeslearn;

public class DataTypesLearn {

	static void datatypesvalue() {
		int a = 45444444;
		System.out.println("Integer = " + a + " 4 Byte store");
		a = 14;
		System.out.println("Integer" + a);

		boolean isVegetarian = true;
		System.out.println("boolearn = " + isVegetarian);

		byte b = 77;
		System.out.println("Byte " + b);

		short c = -10077;
		System.out.println("Short " + c);

		long d = 410000000L;
		System.out.println("Long " + d);

		float e = 4.55f;
		System.out.println("Float = " + e);

		double g = 7896.047777d;
		System.out.println("Double " + g);

		char ch = 'A';
		System.out.println("Character " + ch);
	}

	public static void main(String[] args) {
		datatypesvalue();
	}

}
